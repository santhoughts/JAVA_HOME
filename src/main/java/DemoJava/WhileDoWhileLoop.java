package DemoJava;

public class WhileDoWhileLoop {

    public static void main(String[] args) {

        // While loop
        // While loop is a boolean conditions loop

        int i=0;
        while (i<10)
        {
            System.out.println(i);
            i++;
        }

        // do while loop
        int j=20;
        do{
            System.out.println(j);
            j++;
        }while (j<20); //  1St loop of execution done gurantee


    }
}
